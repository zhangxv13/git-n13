package bean;

public class MpData {
	private int colorid;
	private float num;
	private String prochina;
	public float getNum() {
		return num;
	}
	public void setNum(float num) {
		this.num = num;
	}
	public String getProchina() {
		return prochina;
	}
	public void setProchina(String prochina) {
		this.prochina = prochina;
	}
	public int getColorid() {
		return colorid;
	}
	public void setColorid(int colorid) {
		this.colorid = colorid;
	}
}
