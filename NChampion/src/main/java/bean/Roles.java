package bean;

public class Roles {
	private int rid;
	private String role;
	private String chrole;
	
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getChrole() {
		return chrole;
	}
	public void setChrole(String chrole) {
		this.chrole = chrole;
	}
}
