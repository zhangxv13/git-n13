package bean;

public class Toxin {
	private	int id;
	private String toxintype;
	private int state;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getToxintype() {
		return toxintype;
	}
	public void setToxintype(String toxintype) {
		this.toxintype = toxintype;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
}
