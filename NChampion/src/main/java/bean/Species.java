package bean;

public class Species {
	 private int id;
	 private String cropSpecies;
	 private int catgoryid;
	 private int state;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCropSpecies() {
		return cropSpecies;
	}
	public void setCropSpecies(String cropSpecies) {
		this.cropSpecies = cropSpecies;
	}
	public int getCatgoryid() {
		return catgoryid;
	}
	public void setCatgoryid(int catgoryid) {
		this.catgoryid = catgoryid;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
}
